export const CLASSES_MAP: Record<string, ClassDef> = Object.fromEntries(
  CLASSES.map((c) => [c.id, c]),
);

export function getFeaturesUpToLevel(classId: string, level: number): ClassFeature[] {
  const cls = CLASSES_MAP[classId];
  if (!cls) return [];
  return cls.features.filter((f) => f.level <= level);
}
